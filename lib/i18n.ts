/**
 * Returns a copy of the advisory with localised content where available.
 * Falls back to the English data for any field/language not translated.
 */
export function localizeAdvisory(adv: Advisory, lang: Language): Advisory {
  if (adv.id === "dengue") {
    const c = dengueContent[lang];
    if (c) {
      return {
        ...adv,
        title: c.title,
        summary: c.summary,
        whatToDo: c.whatToDo,
        why: adv.why ? { ...adv.why, text: c.whyText ?? adv.why.text } : adv.why,
      };
    }
  }
  return adv;
}

export interface VideoItem {
  id: string; // stable id, e.g. "self-test-kit" (referenced by the AI chat)
  title: string; // section heading shown above the player
  source: string; // publisher / channel, e.g. "The Straits Times"
  src: string; // self-hosted video file under /public
  poster: string; // self-hosted poster image shown before play
  // One line on what the video teaches — given to the AI so it knows when to
  // surface this video in a reply.
  about: string;
  // Languages (besides English) with a pre-rendered dub MP3 sitting next to
  // the video, named "<basename>.<lang>.mp3". When the app language is one of
  // these, the player mutes the original and plays the dub track in sync.
  // Generated by scripts/dub.mjs.
  dubLangs?: Language[];
}

// Public URL for a media file in the Supabase "media" bucket (under videos/).
// Files are uploaded by scripts/upload-media.mjs. Falls back to the local
// /public copy if the Supabase URL env var is missing (e.g. before setup).
const MEDIA_BUCKET = "media";
function mediaUrl(file: string): string {
  const base = process.env.NEXT_PUBLIC_SUPABASE_URL;
  return base
    ? `${base}/storage/v1/object/public/${MEDIA_BUCKET}/videos/${file}`
    : `/videos/${file}`;
}


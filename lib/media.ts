/** All videos as a flat list, regardless of hazard. */
export const allVideos: VideoItem[] = Object.values(videos).filter(
  (v): v is VideoItem => Boolean(v)
);

/** Look up a video by its stable id (used to render a video the AI suggested). */
export function findVideo(id: string): VideoItem | null {
  return allVideos.find((v) => v.id === id) ?? null;
}

/** Path of the dubbed MP3 for a video + language, e.g. ".../x.mp4" -> ".../x.id.mp3". */
export function dubTrackPath(src: string, lang: Language): string {
  return src.replace(/\.mp4$/, `.${lang}.mp3`);
}

